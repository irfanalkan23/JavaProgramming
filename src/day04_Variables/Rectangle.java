package day04_Variables;

public class Rectangle {

    public static void main(String[] args) {

        double side1 = 5.5;
        double side2 = 10.5;
        double area = side1 * side2;
        double perimeter = (side1 + side2) * 2;
        System.out.println("area = " + area);
        System.out.println("perimeter = " + perimeter);
    }
}
