package Day06_PrimitiveTypeCastings;

public class BirthDay {
    public static void main(String[] args) {
        String name = "John";
        int birthDay = 25;
        String month = "April";
        int birthYear = 1995;

        // name was born on month/day/year.
        System.out.println(name + " was born on " + month + "/" + birthDay + "/" + birthYear);

    }
}
